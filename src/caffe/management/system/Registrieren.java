    public void validateFields() {
        String name = txtName.getText();
        String email = txtEmail.getText();
        String telefonNr = txtTelefonNr.getText();
        String adresse = txtAdresse.getText();
        String kennwort = txtKennwort.getText();
        String sicherheitFrage = txtSicherheitFrage.getText();
        String antwort = txtAntwort.getText();

        if (!name.equals("") && email.matches(emailPattern) && telefonNr.matches(telefonNrPattern) && telefonNr.length() == 10 && !adresse.equals("") && !kennwort.equals("") && !sicherheitFrage.equals("") && !antwort.equals("")) {
            btnSpeichern.setEnabled(true);
        } else {
            btnSpeichern.setEnabled(false);
        }

    }

