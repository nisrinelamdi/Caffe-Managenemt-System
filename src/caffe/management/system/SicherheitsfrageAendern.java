    public void validateField() {
        String kennwort = txtKennwort.getText();
        String sicherheitfrage = txtNeueSicherheitfrage.getText();
        String antwort = txtAntwort.getText();

        if (!kennwort.equals("") && !sicherheitfrage.equals("") && !antwort.equals("")) {
            btnSpeichern.setEnabled(true);
        } else {
            btnSpeichern.setEnabled(false);
        }
    }

